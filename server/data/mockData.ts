// Helper to add a new course
export function addCourse(courseData: Omit<Course, 'id' | 'createdAt' | 'categoryGroup' | 'categoryLabel'>): Course {
    const config = categoryConfig[courseData.category];
    const newCourse: Course = {
        ...courseData,
        id: `course-${Date.now()}`,
        categoryGroup: config.group as Course['categoryGroup'],
        categoryLabel: config.label,
        createdAt: new Date().toISOString(),
    };
    courses.push(newCourse);
    return newCourse;
}

// Helper to find student by ID
export function findStudentById(id: string): Student | undefined {
    return students.find(s => s.id === id);
}
